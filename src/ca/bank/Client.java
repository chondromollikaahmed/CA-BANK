    void loadBlance()
    {
        String query= "select * from blance_table where acc_no=?";
        dbaltr= new DatabaseConnect();
        
        try {
            dbaltr.pst=dbaltr.con.prepareStatement(query );
            dbaltr.pst.setString(1, ClientVar.getAccNo());
            dbaltr.rs=dbaltr.pst.executeQuery();
            
            if(dbaltr.rs.next())
            {
                ClientVar.setBlance(dbaltr.rs.getDouble(2));
                System.out.println(dbaltr.rs.getDouble(2));
                lbl_dash_blance.setText(dbaltr.rs.getDouble(2)+"/-");
                lbl_dash_pending.setText(dbaltr.rs.getDouble(3)+"/-");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }  
        
        
        
                
                    
                
    }
    
    
